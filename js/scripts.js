
$(document).ready(function() {
  var animal = prompt("Type your favorite animal. Turtles, Ferrets, or Squirrels?");

  if (animal === "Turtles") {
    $('#turtle').show();
  }
    else if
    (animal ==="Ferrets"){
      $("#ferret").show();


  }
    else if
    (animal ==="Squirrels"){
      ("#squirrel").show();

  }
    else {
    prompt("Type your favorite animal. Turtles, Ferrets, or Squirrels?");
  }

});
